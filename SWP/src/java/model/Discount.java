package model;

/**
 *
 * @author canduykhanh
 */
public class Discount {
    private int id;
    private int value;

    public Discount() {
    }

    public Discount(int id, int value) {
        this.id = id;
        this.value = value;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
    
    
}
